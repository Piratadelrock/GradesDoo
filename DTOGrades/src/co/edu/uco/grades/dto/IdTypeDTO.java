package co.edu.uco.grades.dto;

public class IdTypeDTO {
	
	private int id;
	private String name;
	
	public IdTypeDTO(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}

	
}
